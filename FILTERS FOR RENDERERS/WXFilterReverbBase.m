}
// ******************************************************************************************
-(BOOL)		isPrime:(int)number
{
int i;
if (number == 2) return YES;
if (number & 1)	{
	for (i=3; i<(int)sqrt((double)number)+1; i+=2)
		if ( (number % i) == 0) return NO;
	return YES; /* prime */
}
else return NO; /* even */
}
